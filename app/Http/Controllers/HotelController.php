    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index($region_id = -1)
    {
        if ($region_id != -1)
            $hotels = Hotel::where('region_id', '=', $region_id)->Latest()->paginate(12);
        else
            $hotels = Hotel::Latest()->paginate(12);

        if (\Auth::user()->admin == 1)
        {
            return view('admin.index', ['hotels'=>$hotels]);
        }else{
            if ($region_id != -1)
                return view('hotels.hotelsByRegion', ['hotels'=>$hotels]);
            else
                return view('');
        }
    }

